                <p>O Abstracionismo, ou arte abstrata, é um estilo artístico moderno das artes visuais que prioriza as formas abstratas em detrimento das figuras que representam algo da nossa própria realidade.

Dessa forma, podemos dizer que esse tipo de arte é uma obra “não representacional”, ao contrário da arte figurativa, expressa por meio de figuras que retratam a natureza.

O pintor russo Wassily Kandinsky é considerado o precursor da arte abstrata com suas obras Primeira Aquarela Abstrata (1910) e a série Improvisações (1909-14).</p>

 <h3>Principais características do abstracionismo</h3>
<p>As principais características da arte abstrata são:</p>

<p><li>Arte não representacional;</li></p>
<p><li>Ausência de objetos reconhecíveis;</li></p>
<p><li>Arte subjetiva;</li></p>
<p><li>Oposição ao modelo renascentista e à arte figurativa;</li></p>
<p><li>Valorização de formas, cores, linhas e texturas.</li></p>
