        <p class="text-justify">A Pop Art é um slogan engenhoso, irônica e crítica. Uma réplica dos slogans dos meios de comunicação, cujas histórias fazem história,     cuja estética condiciona as artes plásticas e a imagem da época, e cujos "clichês" influenciam as pessoas.
            A cultura Pop e o modo de vida ficaram intimamente ligados aos anos sessenta. O Pop se caracteriza na reação de uma época que se estendeu a nossa existência, tanto no processo social como no âmbito privado. Na história da arte, não existiu antes uma superposição semelhante, uma proximidade entre vida e arte tão evidente para todos. Tão palpável e tão genial. Os temas, as formas e os meios do Pop Art mostraram as características essenciais que associamos com o ambiente cultural dos anos sessenta e o estado de ânimo das pessoas. O Pop é uma manifestação cultural absolutamente ocidental que foi crescendo devido as condições capitalistas e tecnológicas da sociedade industrial. A América é o centro desta manifestação. O Pop Art analisa artisticamente a situação de americanização,, visualiza um sismograma das nossas conquistas industriais modernas e seu absurdo, nos limites de uma sociedade de massas e meios de comunicação. O Pop Art vive das grandes cidades. Em seu começo, foram Nova Iorque e Londres os novos centros artísticos do mundo ocidental, no seu desenvolvimento durante os anos sessenta se incorporaram outros centros europeus secundários.
            Para entender a importância deste cambio cultural e impulso da arte de uma nova era, deve-se obter referência de alguns aspectos gerais que mostraram a multiplicidade dos câmbios no humor artístico, social e individual. A estabilização política e econômica da época de pós-guerra conduziu revalorização daquilo que em geral pode-se designar como o Popular. Segundo Tilman Osterwold, em seu livro "Pop Art", o termo inglês para população em massa é "POPulace" e "popular" é algo que conta com a aceitação geral. Aqui surgiu em forma de manifesto, o movimento Pop Art.
            Com o objetivo da crítica irônica do bombardeamento da sociedade pelos objetos de consumo, ela operava com signos estéticos massificados da publicidade, quadrinhos e ilustrações, usando como materiais principais, tinta acrílica, poliéster, látex, produtos com cores intensas, brilhantes e vibrantes, reproduzindo objetos do cotidiano em tamanho consideravelmente grande, transformando o real em hiper-real. Mas ao mesmo tempo que produzia a crítica, a Pop Art se apoiava e necessitava dos objetivos de consumo, nos quais se inspirava e muitas vezes o próprio aumento do consumo, como aconteceu por exemplo, com as Sopas Campbell, de Andy Warhol, um dos principais artistas da Pop Art. Além disso, muito do que era considerado brega, virou moda, e já que tanto o gosto, como a arte tem um determinado valor e significado conforme o contexto histórico em que se realiza, a Pop Art proporcionou a transformação do que era considerado vulgar, em refinado, e aproximou a arte das massas, desmitificando, já que se utilizava de objetos próprios delas, a arte para poucos. </p>
            
